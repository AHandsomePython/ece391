// if nbytes == 3 * sizeof(int32_t) : Byte 0-3: x, Byte 4-7: y, Byte 8->11: color
// if nbytes == IMAGE_X_DIM * IMAGE_Y_DIM : copy the picture to the window buf and show on the screen
int GUI_window_write(int32_t fd, const void* buf, int32_t nbytes){
    if(buf==NULL || current->window==NULL) return -1;
    if(nbytes == 3*sizeof(int32_t)){
        int32_t* arr = (int32_t*)buf;
        int x = arr[0];
        int y = arr[1];
        unsigned char color = (unsigned char)arr[2];
        if(x<0 || x>=IMAGE_X_DIM || y<0 || y>=IMAGE_Y_DIM) return -1;
        GUI_window_draw_dot(current->window, x, y, color);
        return 0;
    }else if(nbytes == IMAGE_X_DIM * IMAGE_Y_DIM){
        memcpy(current->window->buf, buf, nbytes);
        if(window_get_top()==current->window){
            GUI_window_show(current->window);
            GUI_screen_show();
        }
        return 0;
    }
    return -1;
}




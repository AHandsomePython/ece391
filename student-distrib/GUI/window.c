// 此函数维护虚拟窗口和物理窗口的关系，x y是虚拟窗口的坐标，虚拟窗口可以理解为每个窗口都有整个屏幕那么大
// 所有绘图的函数调用都需要基于此函数以实现正确的窗口映射
int GUI_window_draw_dot(GUI_window_t* window, int x, int y, unsigned char color){
    if(window == NULL) return -1;
    if(x >= IMAGE_X_DIM || x < 0 || y >= IMAGE_Y_DIM || y < 0) return -1;
    window->buf[y*IMAGE_X_DIM+x] = color;
    int actual_x = (float)x * window->Pw / IMAGE_X_DIM + window->Px;
    int actual_y = (float)y * window->Ph / IMAGE_Y_DIM + window->Py;
    if(window_get_top()==window){
        GUI_screen_draw_dot(actual_x, actual_y, color);
    }
    return 0;
}

int GUI_window_draw_font(GUI_window_t* window, int x, int y, int w, int h, char c, unsigned char color){
    if(window == NULL) return -1;
    int i, j;
    int font_x, font_y;
    for(i=0; i<h; i++){
        for(j=0; j<w; j++){
            font_x = j * FONT_WIDTH / w;
            font_y = i * FONT_HEIGHT / h;
            if((font_data[(unsigned char)c][font_y] >> (FONT_WIDTH - font_x - 1)) & 0x1){
                GUI_window_draw_dot(window, x+j, y+i, color);
            }
        }
    }
    return 0;
}

int GUI_window_draw_font_over(GUI_window_t* window, int x, int y, int w, int h, char c, unsigned char color, unsigned char background){
    if(window == NULL) return -1;
    int i, j;
    int font_x, font_y;
    for(i=0; i<h; i++){
        for(j=0; j<w; j++){
            font_x = j * FONT_WIDTH / w;
            font_y = i * FONT_HEIGHT / h;
            if((font_data[(unsigned char)c][font_y] >> (FONT_WIDTH - font_x - 1)) & 0x1){
                GUI_window_draw_dot(window, x+j, y+i, color);
            }else{
                GUI_window_draw_dot(window, x+j, y+i, background);
            }
        }
    }
    return 0;
}

int GUI_window_change_pos(GUI_window_t* window, int Wx, int Wy, int Ww, int Wh){
    if(window == NULL || Ww<=0 || Wh<=0) return -1;
    window->Wx = Wx;
    window->Wy = Wy;
    window->Ww = Ww;
    window->Wh = Wh;
    window->Px = W2P_x(Wx);
    window->Py = W2P_y(Wy);
    window->Pw = W2P_w(Ww);
    window->Ph = W2P_h(Wh);
    GUI_screen_clear();
    _GUI_window_bar_print(window);
    GUI_window_show(window);
    GUI_screen_show();
    return 0;
}


#define W2P_x(Wx) (Wx + WINDOW_BAR_SIDE_GAP)
#define W2P_y(Wy) (Wy + WINDOW_BAR_HEIGHT)
#define W2P_w(Ww) (Ww - 2 * WINDOW_BAR_SIDE_GAP)
#define W2P_h(Wh) (Wh - WINDOW_BAR_HEIGHT - WINDOW_BAR_SIDE_GAP)


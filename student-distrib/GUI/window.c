static GUI_window_t GUI_window_arr[WINDOW_MAX_NUM];
static int GUI_window_used[WINDOW_MAX_NUM];

GUI_window_t* GUI_window_get(int index){
    if(index<0 || index>=WINDOW_MAX_NUM) return NULL;
    return &GUI_window_arr[index];
}

int32_t GUI_window_get_index(GUI_window_t* window){
    int32_t index = ((int32_t)window - (int32_t)GUI_window_arr) / sizeof(GUI_window_t);
    if(index<0 || index>=WINDOW_MAX_NUM) return -1;
    return index;
}

GUI_window_t* GUI_window_alloc(){
    int i=0;
    for(i=0; i<WINDOW_MAX_NUM; i++){
        if(GUI_window_used[i]==0){
            GUI_window_used[i] = 1;
            return &GUI_window_arr[i];
        }
    }
    return NULL;
}

int GUI_window_free(GUI_window_t* window){
    int32_t index = ((int32_t)window - (int32_t)GUI_window_arr) / sizeof(GUI_window_t);
    if(index<0 || index>=WINDOW_MAX_NUM) return -1;
    GUI_window_used[index] = 0;
    return 0;
}

int _GUI_window_bar_draw_dot(GUI_window_t* window, int x, int y, unsigned char color){
    int actual_x = x + window->Wx;
    int actual_y = y + window->Wy;
    GUI_screen_draw_dot(actual_x, actual_y, color);
    return 0;
}

int _GUI_window_bar_draw_font(GUI_window_t* window, int cur_x, int w, int h, char c, unsigned char color){
    int i, j;
    int font_x, font_y;
    for(i=0; i<h; i++){
        for(j=0; j<w; j++){
            font_x = j * FONT_WIDTH / w;
            font_y = i * FONT_HEIGHT / h;
            if((font_data[(unsigned char)c][font_y] >> (FONT_WIDTH - font_x - 1)) & 0x1){
                _GUI_window_bar_draw_dot(window, cur_x+j, i, color);
            }
        }
    }
    return 0;
}

int _GUI_window_bar_print(GUI_window_t* window){
    int length = strlen(window->name);
    int fh = window->Wh - window->Ph - 1;
    int fw = fh * FONT_WIDTH / FONT_HEIGHT;
    int cur_x = (window->Ww / 2) - (length * fw / 2);
    int i, j;
    for(i=window->Wy; i<window->Py; i++){
        for(j=window->Wx; j<window->Wx+window->Ww; j++){
            GUI_screen_draw_dot(j, i, WINDOW_BAR_BACKGROUND_COLOR);
        }
    }
    int button_x = get_button_start_x(window);
    int button_width = WINDOW_BAR_BUTTON_WIDTH;
    int button_height = WINDOW_BAR_BUTTON_HEIGHT;
    GUI_screen_draw_rect(button_x, window->Wy, button_width, button_height, WINDOW_BAR_BUTTON1_COLOR);
    GUI_screen_draw_rect(button_x+button_width, window->Wy, button_width, button_height, WINDOW_BAR_BUTTON2_COLOR);
    GUI_screen_draw_rect(button_x+button_width*2, window->Wy, button_width, button_height, WINDOW_BAR_BUTTON3_COLOR);
    GUI_screen_draw_line(button_x+button_width*1, window->Wy, button_x+button_width*1, window->Wy+button_height, WINDOW_BAR_BACKGROUND_COLOR);
    GUI_screen_draw_line(button_x+button_width*2, window->Wy, button_x+button_width*2, window->Wy+button_height, WINDOW_BAR_BACKGROUND_COLOR);

    for(i=window->Wy; i<window->Wy+window->Wh; i++){
        for(j=window->Wx; j<window->Px; j++){
            GUI_screen_draw_dot(j, i, WINDOW_BAR_BACKGROUND_COLOR);
        }
        for(j=window->Px+window->Pw; j<window->Wx+window->Ww; j++){
            GUI_screen_draw_dot(j, i, WINDOW_BAR_BACKGROUND_COLOR);
        }
    }
    for(i=window->Py+window->Ph; i<window->Wy+window->Wh; i++){
        for(j=window->Wx; j<window->Wx+window->Ww; j++){
            GUI_screen_draw_dot(j, i, WINDOW_BAR_BACKGROUND_COLOR);
        }
    }
    for(i=0; i<length; i++){
        _GUI_window_bar_draw_font(window, cur_x, fw, fh, window->name[i], WINDOW_BAR_TEXT_COLOR);
        cur_x += fw;
    }
    return 0;
}


int GUI_window_init(GUI_window_t* window, const char* name, int owner_pid){
    if(window == NULL || strlen(name)>WINDOW_NAME_SIZE) return -1;
    window->Wx = WINDOW_DEFULT_X;
    window->Wy = WINDOW_DEFULT_Y;
    window->Ww = WINDOW_DEFULT_WIDTH;
    window->Wh = WINDOW_DEFULT_HEIGHT;
    window->Px = W2P_x(window->Wx);
    window->Py = W2P_y(window->Wy);
    window->Pw = W2P_w(window->Ww);
    window->Ph = W2P_h(window->Wh);
    window->is_show = 1;
    window->owner_pid = owner_pid;
    memset(window->name, 0, WINDOW_NAME_SIZE);
    memset(window->buf, 0, IMAGE_X_DIM*IMAGE_Y_DIM);
    if(name!=NULL) strcpy(window->name, name);
    window_switch(window);
    GUI_screen_show();
    // _GUI_window_bar_print(window);
    // GUI_window_show(window);
    return 0;
}

int GUI_window_exit(GUI_window_t* window){
    window_remove(window);
    return 0;
}


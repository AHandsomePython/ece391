// This function from Zhenyu Zhang's MP5 in ECE220
int32_t
near_horizontal(int32_t x_start, int32_t y_start, int32_t x_end, int32_t y_end, unsigned char color){
	/* Your code goes here! */
	int32_t x,y,x1,y1,x2,y2,sgn,m;
	m=1;							//decide whether the pixels are out of bound
	if(x_start<=x_end)				//set x1=min[x_start,x_end]
	{
		x1=x_start;
		y1=y_start;
		x2=x_end;
		y2=y_end;
	}
	else
	{	
		x2=x_start;
		y2=y_start;
		x1=x_end;
		y1=y_end;
	}
	if(y2>y1)						//sign=0,if y2=y1; sign=1, if y2>y1; sgin=-1, if y2<y1
	{
		sgn=1;
	}
	else if (y2==y1)
	{
		sgn=0;
	}
	else if (y2<y1)
	{
		sgn=-1;
	}
	for(x=x1;x<x2;x++)
	{
		y=(2*(y2-y1)*(x-x1)+(x2-x1)*sgn)/(2*(x2-x1))+y1;	//set value of y
		m=m*GUI_screen_draw_dot(x, y, color);								//if n=0, the program will return 0
	}
	return m;
}

// This function from Zhenyu Zhang's MP5 in ECE220
int32_t
near_vertical(int32_t x_start, int32_t y_start, int32_t x_end, int32_t y_end, unsigned char color){
	int32_t x,y,x_1,y_1,x_2,y_2,sgn,m;
	m=1;										//decide whether the pixels are out of bound
	if (x_start==x_end&&y_start==y_end)		//if the two points are identical, then draw a single point
	{
		m=GUI_screen_draw_dot(x_start, y_start, color);
		return m;
	}
	if(y_start<=y_end)							//set y1=min[y_start,y_end]
	{
		x_1=x_start;
		y_1=y_start;
		x_2=x_end;
		y_2=y_end;
	}
	else
	{	
		x_2=x_start;
		y_2=y_start;
		x_1=x_end;
		y_1=y_end;
	}
	if(x_2>x_1)					//sign=0,if x2=x1; sign=1, if x2>x1; sgin=-1, if x2<x1			
	{
		sgn=1;
	}
	else if (x_2==x_1)
	{
		sgn=0;
	}
	else if (x_2<x_1)
	{
		sgn=-1;
	}
	for(y=y_1;y<y_2;y++)
	{
		x=(2*(x_2-x_1)*(y-y_1)+(y_2-y_1)*sgn)/(2*(y_2-y_1))+x_1;	//set value of x
		m=m*GUI_screen_draw_dot(x, y, color);											//if n=0, the program will return 0
	}
	return m;
}

// This function from Zhenyu Zhang's MP5 in ECE220
int
GUI_screen_draw_line(int x_start, int y_start, int x_end, int y_end, unsigned char color){
	/* Your code goes here! */
	if(x_end==x_start)
	{
		if(!near_vertical(x_start,y_start,x_end,y_end,color))
		{
			return 0;
		}
	}
	else if((y_end-y_start)/(x_end-x_start)<=1&&(y_end-y_start)/(x_end-x_start)>=-1)
	{
		if(!near_horizontal(x_start,y_start,x_end,y_end,color))
		{
			return 0;
		}
	}
	else
	{
		if(!near_vertical(x_start,y_start,x_end,y_end,color))
		{
			return 0;
		}
	}
	return 1;
}


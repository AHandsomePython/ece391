static unsigned char build_screen_buf[IMAGE_X_DIM*IMAGE_Y_DIM];
int GUI_screen_show(){
    memcpy(build_screen_buf, cur_screen_buf, IMAGE_X_DIM * IMAGE_Y_DIM);
    int i, j;
    mouse_t* mouse_ptr = get_mouse();
    int x = mouse_ptr->x;
    int y = mouse_ptr->y;
    int mouse_color = get_mouse_color();
    for(i=0;i<4;i++){
        for(j=0;j<4;j++){
            plane_buf_draw_dot(build_screen_buf, x+i, y+j, mouse_color);
        }
    }
    screen_display((char*)build_screen_buf);
    return 0;
}



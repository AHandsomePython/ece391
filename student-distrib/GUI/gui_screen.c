//
// Helper functions:
//
int plane_buf_draw_dot(unsigned char* buf, int x, int y, unsigned char color){
    if(x >= IMAGE_X_DIM || x < 0 || y >= IMAGE_Y_DIM || y < 0) return -1;
    int buf_index = (3-(x&3))*IMAGE_PLANE_SIZE + (x>>2) + y*IMAGE_X_WIDTH;
    buf[buf_index] = color;
    return 0;
}

int plane_buf_draw_rect(unsigned char* buf, int x, int y, int w, int h, unsigned char color){
    int i, j;
    for(i=y; i<y+h; i++){
        for(j=x; j<x+w; j++){
            plane_buf_draw_dot(buf, j, i, color);
        }
    }
    return 0;
}

//
// Background and Status Bar
//
// static unsigned char palette_button_img[STATUS_BAR_BLOCK_START_X*DESKTOP_STATUS_BAR_HEIGHT];
int GUI_build_background(unsigned char* img){
    if(img==NULL){
        memset(background_img, 0, IMAGE_X_DIM * IMAGE_Y_DIM);
    }else{
        memcpy(background_img, img, IMAGE_X_DIM * IMAGE_Y_DIM);
    }
    plane_buf_draw_rect(background_img, 0, IMAGE_Y_DIM-DESKTOP_STATUS_BAR_HEIGHT, IMAGE_X_DIM, DESKTOP_STATUS_BAR_HEIGHT, DESKTOP_STATUS_BAR_COLOR);
    plane_buf_draw_rect(background_img, 0, IMAGE_Y_DIM-DESKTOP_STATUS_BAR_HEIGHT, DESKTOP_STATUS_BAR_HEIGHT, DESKTOP_STATUS_BAR_HEIGHT, 0x3F);
    return 0;
}

//
// Status bar functions
//
int GUI_status_bar_draw_dot(int x, int y, unsigned char color){
    return plane_buf_draw_dot(background_img, x, y+IMAGE_Y_DIM-DESKTOP_STATUS_BAR_HEIGHT, color);
}

int GUI_status_bar_draw_font(int cur_x, int w, int h, char c, unsigned char color){
    int i, j;
    int font_x, font_y;
    for(i=0; i<h; i++){
        for(j=0; j<w; j++){
            font_x = j * FONT_WIDTH / w;
            font_y = i * FONT_HEIGHT / h;
            if((font_data[(unsigned char)c][font_y] >> (FONT_WIDTH - font_x - 1)) & 0x1){
                GUI_status_bar_draw_dot(cur_x+j, i, color);
            }
        }
    }
    return 0;
}

int GUI_status_bar_draw_font_defult_size(int cur_x, char c, unsigned char color){
    return GUI_status_bar_draw_font(cur_x, FONT_WIDTH, DESKTOP_STATUS_BAR_HEIGHT, c, color);
}

//
//  Desktop Init Part
//
int GUI_desktop_init(){
    memcpy(desktop_img, background_img, IMAGE_X_DIM * IMAGE_Y_DIM);
    int i, j, x;
    for(i=0; i<DESKTOP_STATUS_BAR_HEIGHT; i++){
        for(j=0; j<IMAGE_X_DIM; j++){
            plane_buf_draw_dot(desktop_img, j, i, DESKTOP_STATUS_BAR_COLOR);
        }
    }
    for(i=1; i<MAX_PROCESS_NUM; i++){
        x = i * IMAGE_X_DIM / MAX_PROCESS_NUM;
        for(j=0; j<DESKTOP_STATUS_BAR_HEIGHT; j++){
            plane_buf_draw_dot(desktop_img, x, j, DESKTOP_STATUS_BAR_SPLIT_COLOR);
            plane_buf_draw_dot(desktop_img, x-1, j, DESKTOP_STATUS_BAR_SPLIT_COLOR);
            plane_buf_draw_dot(desktop_img, x+1, j, DESKTOP_STATUS_BAR_SPLIT_COLOR);
        }
    }
    return 0;
}

